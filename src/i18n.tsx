type TranslationKey = keyof typeof TRANSLATIONS.en

function getStoredLanguage(): LanguageCode {
  const value = localStorage.getItem(STORAGE_KEY)
  if (value === 'hi' || value === 'mr' || value === 'en') return value
  return 'en'
}

function replaceParams(value: string, params?: Record<string, string | number>) {
  if (!params) return value
  return Object.entries(params).reduce(
    (acc, [key, paramValue]) => acc.replaceAll(`{${key}}`, String(paramValue)),
    value,
  )
}

interface LanguageContextValue {
  language: LanguageCode
  setLanguage: (language: LanguageCode) => void
  t: (key: TranslationKey | string, params?: Record<string, string | number>) => string
}

const LanguageContext = createContext<LanguageContextValue | null>(null)

export function LanguageProvider({ children }: { children: React.ReactNode }) {
  const [language, setLanguageState] = useState<LanguageCode>(getStoredLanguage())

  useEffect(() => {
    localStorage.setItem(STORAGE_KEY, language)
    document.documentElement.lang = language
    document.documentElement.dir = 'ltr'
  }, [language])

  const setLanguage = (nextLanguage: LanguageCode) => {
    setLanguageState(nextLanguage)
  }

  const t = (key: TranslationKey | string, params?: Record<string, string | number>) => {
    const table = TRANSLATIONS[language] || TRANSLATIONS.en
    const raw = table[key] ?? TRANSLATIONS.en[key as TranslationKey] ?? key
    return replaceParams(raw, params)
  }

  const value = { language, setLanguage, t }

  return <LanguageContext.Provider value={value}>{children}</LanguageContext.Provider>
}

export function useLanguage() {
  const ctx = useContext(LanguageContext)
  if (!ctx) {
    throw new Error('useLanguage must be used within LanguageProvider')
  }
  return ctx
}

export function useI18n() {
  return useLanguage()
}

